//! Java Basics

//? The main keywords in Java are:

// * 1. public: An access modifier that allows the class or method to be accessible from any other class.

// * 2. class: A keyword used to define a class.

// * 3. static: A keyword that indicates that a method or variable belongs to the class itself rather than to instances of the class.

// * 4. void: A keyword that indicates that a method does not return any value.

// * 5. main: The name of the main method, which is the entry point of a Java program.

// * 6. String: A class in Java that represents a sequence of characters.

// * 7. args: An array of strings that can be passed as command-line arguments to the main method.

// * 8. System: A class that provides access to the system resources.

// * 9. out: A static member of the System class that represents the standard output stream.

// * 10. println: A method of the PrintStream class that prints a message to the console and moves the cursor to the next line.

// * 11. import: A keyword used to include other classes or packages in the current class.

// * 12. Scanner: A class in the java.util package that is used to read input from various sources, including user input from the console.

// * 13. Exception: A class that represents an error or an exceptional condition that occurs during the execution of a program.

// * 14. try: A keyword used to define a block of code that may throw an exception.

// * 15. catch: A keyword used to define a block of code that handles an exception thrown by the try block.

// * 16. throw: A keyword used to explicitly throw an exception.

// * 17. throws: A keyword used in method declarations to specify that a method may throw one or more exceptions.

// * 18. new: A keyword used to create new objects or instances of a class.

// * 19. this: A keyword that refers to the current instance of a class.

// * 20. super: A keyword that refers to the superclass of the current object.

// * 21. return: A keyword used to exit a method and optionally return a value.

// * 22. null: A literal that represents a null reference, indicating that a variable does not point to any object.

// * 23. package: A keyword used to define a namespace for classes and interfaces.

// * 24. import: A keyword used to include other classes or packages in the current class.

// * 25. extends: A keyword used to indicate that a class is inheriting from a superclass.

public class JavaBasics {
    public static void main(String[] args) {
        // The main method is where the program starts executing.
        // It takes an array of strings as arguments, which can be used to pass command-line arguments.
        System.out.println("Hello, world!"); // Print a message to the console.
    }
}
//? The output of the program will be:
//* */ Output: Hello, world!